"""
Crear un programa que pida al usuario ingresar 2 números 
por teclado y devuelva por pantalla la suma de ellos en 
un renglón, la resta en otro, el producto en otros y la 
división en otro.
"""

num1 = int(input("Ingresa un número: "))
num2 = int(input("Ingresa otro número"))
print(num1 + num2)
print(num1 - num2)
print(num1 / num2)
print(num1 % num2)


